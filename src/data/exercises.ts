export const allExercises: Exercise[] = [
  ...chestExercises,
  ...backExercises,
  ...shoulderExercises,
  ...armExercises,
  ...legExercises,
]

// Helper to find exercise by ID
export function getExerciseById(id: string): Exercise | undefined {
  return allExercises.find((e) => e.id === id)
}

// Helper to get exercises by muscle group
export function getExercisesByMuscle(muscle: string): Exercise[] {
  return allExercises.filter(
    (e) => e.muscleGroupPrimary === muscle || e.muscleGroupsSecondary.includes(muscle as any)
  )
}

// Helper to get exercises by equipment
export function getExercisesByEquipment(equipment: string): Exercise[] {
  return allExercises.filter((e) => e.equipment === equipment)
}
